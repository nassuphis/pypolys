factor = 1

def get_letter_coordinates(letter):
    """Returns a list of complex numbers representing coordinates of 'on' bits for the given letter."""
    letter = letter.lower()
    if letter not in FONT:
        raise ValueError(f"Letter '{letter}' is not supported.")
    
    coordinates = []
    for y, row in enumerate(FONT[letter]):
        for x in range(8):
            if row & (1 << (7-x)):  # Check if the x-th bit (from left) is set
                x_center = x - 3.5
                y_center = (7-y) - 3.5
                x_scaled = x_center * factor
                y_scaled = y_center * factor
                z = complex(x_scaled, y_scaled)
                coordinates.append(z) 
    return np.array(coordinates, dtype=np.complex128)


FONTXY = {letter: get_letter_coordinates(letter) for letter in FONT}

def scale(t,s):
    return ( (1-s)/2+ s*t ) * factor

def square( x, t1, t2, offset ):
    cf = FONTXY[x] +  complex( scale(t1,0.9) , scale(t2,0.9) )
    return np.array( cf + offset, dtype=complex )

def sml_square( x, t1, t2, offset ):
    cf = FONTXY[x] + complex( scale(t1,0.2), scale(t2,0.2) )
    return np.array( cf + offset, dtype=complex )

def tiny_square( x, t1, t2, offset ):
    cf = FONTXY[x] + complex( scale(t1,0.1) , scale(t2,0.1) )
    return np.array( cf + offset, dtype=complex )


def circle( x, t1, t2, offset ):
    pi2 = 2 * np.pi 
    pert = 0.1 * np.real(t1) * np.exp( 1j* pi2 * np.real(t2) )
    roots = FONTXY[x]
    seq = np.arange( 1, len(roots) + 1 )
    circles = 10 * np.array( np.sin( pi2 * t1 * seq), dtype=complex)
    symmetrized_circles = ( circles + np.flip(circles) )
    cf = roots + pert * symmetrized_circles
    return np.array( cf + offset, dtype=complex )

